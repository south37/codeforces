int main(int argc, char** argv) {
  ll n, m;
  cin >> n >> m;
  vector< vector<ll> > tree(n);
  rep(i, m) {
    ll x, y;
    cin >> x >> y;
    --x; --y;
    if (x > y) { swap(x, y); }
    // Now, x < y
    tree[y].push_back(x);
  }

  UnionFind z(n);
  set<ll> roots;
  roots.insert(0);
  for (int v = 1; v < n; ++v) {
    set<ll> r = roots; // copy
    map<ll, ll> d; // distribution. root => cnt
    for (auto u : tree[v]) {
      ++d[z.root(u)];
    }
    for (auto x : d) {
      if (x.second == z.count(x.first)) { // The numbber of 1-edges is equal to the number of all components of x.first
        r.erase(x.first);
      }
    }
    for (auto nv : r) {
      z.unite(nv, v);
      roots.erase(nv);
    }

    roots.insert(z.root(v));
  }

  ll k = z.size();
  cout << k - 1 << endl;
}
