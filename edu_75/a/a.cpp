void solve() {
  string s;
  cin >> s;
  ll n = s.size();

  // Run Length Encoding
  // e.g. issii -> i1s2i2
  vector< pair<char, ll> > cnts;
  rep(i, n) {
    ll cnt = 1;
    while (i < n - 1 && s[i] == s[i+1]) {
      ++cnt;
      ++i;
    }
    cnts.emplace_back(s[i], cnt);
  }

  set<char> ans;
  for (auto v : cnts) {
    if (v.sc % 2 != 0) { // may broken.
      ans.insert(v.fr);
    }
  }

  for (char e : ans) {
    cout << e;
  }
  cout << endl;
}

